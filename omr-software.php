<h2></h2>
</header>
<ul>
<li><strong>OCR</strong>. OCR (optical character recognition) has been added to the software in order to automatically recognize machine-printed characters.</li>
<li><strong>Printing and exporting multiple reports.</strong> The ability to print, export, and generate multiple reports has been added to Remark Quick Stats in the form of a Report Batch Wizard. Reports can be exported to a single combined file or individually to separate files. Report batches can be saved and reloaded.</li>
<li><strong>Auto filtering of data.</strong> The ability to auto-filter reports has been integrated into the new Report Batch Wizard in Remark Quick Stats. You can select up to three separate fields by which to break down data into separate reports. For each unique set of records, a new report set will be generated automatically. For example, if you process course evaluations, Remark Quick Stats can automatically separate each instructor’s results into individual reports.</li>
<li><strong>Duplicate detection.</strong> You can optionally flag duplicate entries in a data set to alert you to a potential problem (e.g., when scanning tests a student’s ID number comes up twice).</li>
<li><strong>Answer key utility.</strong> The Grade Wizard has been broken out into its own utility, which can be accessed without launching the entire Remark Office OMR application. In addition, the Answer Key utility/Grade Wizard can now save what is called a Generic answer key file (*.gaky) that does not save any information for ungraded questions. This allows a generic answer key to be applied to any form type with enough graded questions and answer choices.</li>
<li><strong>Reusing learning objectives. </strong>Learning objectives/Analysis group names can be imported from a database or spreadsheet format into the Grade Wizard/Survey Wizard. You may select the objectives you wish to use from a list upon import.</li>
<li><strong>Ranking question type.</strong> A ranking question type was added to the OMR region types so that you can have respondents rank items in a list.</li>
<li><strong>New barcode types.</strong> New barcode functionality has been added so that the software reads many new 1D and 2D barcode types. Textual barcodes can now contain multiple items separated by a delimiter (e.g., you can have multiple textual items in one barcode and separate them into separate fields in the data grid).</li>
</ul>
<p><strong>Enhanced Features:</strong></p>
<ul>
<li><strong>Review exceptions updates. </strong>Several updates have been made to the Review Exceptions functionality:
<ul>
<li><strong>Skipping exception flag types.</strong> When reviewing exceptions in the data, you can optionally skip certain flag types and the flag will be removed. This feature allows you to review data multiple times without having to review areas that have already been reviewed.</li>
<li><strong>Reviewing exceptions with database lookup.</strong> When reviewing a database lookup error, once the correct lookup value is set, the appended fields from the database will be filled automatically based on the entry entered for the lookup region.</li>
<li><strong>Exception flags update.</strong> The exception flags in the Data Center have been replaced with user-friendly color-coded cells.</li>
</ul>
</li>
<li><strong>Flexible report header use.</strong> Report headers are now much more flexible and can be tied to individual tests or surveys. You can now define report headers within the Grade or Survey Wizard, allowing you to create and reuse specific report headers that will always be used with that particular test or survey.</li>
<li><strong>Name builder.</strong> A Name Builder has been added to the Read Wizard that when clicked will display a new dialog that can be used to create dynamic image names and paths based on recognized data. You can add fields from your data set to the image base name and to the path statement.</li>
<li><strong>Package and rename images.</strong> An option has been added to the Read Wizard called &#8220;Package and rename images&#8221;. When reading from images, the images will be repacked into a new folder based on your selections. For example, a set of single page PCX files could be repackaged as a multiple page PDF file where each PDF is named based on the respondent’s first and last name (from the data set).</li>
<li><strong>Allow blanks option.</strong> An &#8220;Allow Blanks&#8221; option has been added to the Template Editor so that you can specify not to flag blank responses for certain questions (e.g., blanks might be okay because a large portion of the respondents will end up skipping this question based on responses to previous question(s)).</li>
</ul>
