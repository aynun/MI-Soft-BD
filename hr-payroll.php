<p style="text-align: center;"><span style="font-size: 24pt; color: #99cc00;"><strong>Feature of HR &amp; Payroll Software</strong></span></p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Company information</strong></span>: This major module concentrates on providing there with all the necessary information that they would like to know about their organization in view information. they also can edit there information.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Setup</strong></span>: In setup customer can add information like district, blood group, religion, branch, designation, post, weekend, holy day etc. They can also system setting.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Employee Information:</strong></span> This major module concentrates on providing them, with all the necessary information that they would like to know about any single employee from their organization in employee list. They can also add employee Information in add employee</p>
<p style="text-align: justify;"><strong><span style="color: #ff6600;">Attendance</span>:</strong> In attendance function customer can easy way to manage the attendance of their employees. The module gives them all the functionalities required to meet there customized attendance management needs like shift schedule, shift distribute, shift delete, attendance summary, attendance process and upload data.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Leave</strong></span><strong>:</strong> In leave function customer can easy to manage leave setup, calculation and transaction their employees.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Allowance</strong></span><strong>:</strong> All the information related to any allowances and benefits provided to employees maintained in this module. This could include details about house rent, allowance, education allowance, allowance distribute, allowance log.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Deduction</strong></span><strong>:</strong> All kind of deduction process like deduction setup, distribute and deduction log maintained in this function.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Loan</strong></span><strong>:</strong> All the information related to any loan such as loan type and distribute are maintained in this module.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Salary</strong><strong>:</strong></span> salary management system that helps keeps track of all their employees’ salary transactions. In this system maintained salary process, allowance sheet, deduction sheet, pay slip, bank sheet.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>User Manage:</strong></span> In this system a user can control all of the function. To control all of function user have use panel and access control.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>HR Report:</strong></span> HR has all the options to control human resource management solution. They can make any types of combinations to create there custom rules to meet there specific HR needs, which suit their organization. The module gives them all the functionalities such as, present, absent and leaves report. weekend &amp; holyday report, punch, attendance and late report.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Salary Report:</strong></span> salary management system that helps keeps track of all their employees’ salary transactions. In this system maintained salary process, allowance sheet, deduction sheet, pay slip, bank sheet and loan report.</p>
<p style="text-align: justify;"><span style="color: #ff6600;"><strong>Provident Fund:</strong> </span>This module with all the necessary information in provident fund check balance, withdrawals, checks statement, PF deposit, PF loan.</p>
<p style="text-align: justify;"><strong><span style="color: #ff6600;">Facility</span>:</strong></p>
<ol style="text-align: justify;">
<li>Unlimited User</li>
<li>Access from anywhere</li>
<li>Unlimited PC access</li>
<li>Individual access &amp; user panel</li>
<li>Low cost &amp; best support</li>
</ol>
